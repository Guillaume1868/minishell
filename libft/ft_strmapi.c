#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	char		*res;
	int			idx;

	if (!s || !f)
		return (NULL);
	res = (char *)malloc(sizeof(char) * (ft_strlen(s) + 1));
	if (!res)
		return (NULL);
	idx = -1;
	while (s[++idx])
		res[idx] = f(idx, s[idx]);
	res[idx] = '\0';
	return (res);
}
